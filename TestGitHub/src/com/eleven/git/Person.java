package com.eleven.git;

public class Person {
    private Integer id;
    private String name;
    private String address;
    private Integer age;

    public Person() {
    }

    public Person(Integer id, String name, String address, Integer age) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.age = age;
    }
}
